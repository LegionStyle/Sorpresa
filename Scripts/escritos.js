// Mostrar los poemas en el grid
function renderPoemas() {
    const container = document.getElementById('poemas-container');
    container.innerHTML = '';

    poemas.forEach(poema => {
        const card = document.createElement('div');
        card.className = 'poema-card';
        card.innerHTML = `
                    <h3>${poema.titulo}</h3>
                    <p class="resumen">${poema.resumen}</p>
                    <button class="leer-mas" data-id="${poema.id}">Leer completo</button>
                `;
        container.appendChild(card);
    });
}

// Mostrar el modal con el poema completo
function showPoemaModal(id) {
    const poema = poemas.find(p => p.id === id);
    if (!poema) return;

    const modal = document.getElementById('poema-modal');
    const modalTitulo = document.getElementById('modal-titulo');
    const modalContenido = document.getElementById('modal-contenido');

    modalTitulo.textContent = poema.titulo;
    modalContenido.textContent = poema.contenido;

    // Mostrar modal con animaci√≥n
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';

    // Enfocar el modal para accesibilidad
    setTimeout(() => {
        document.querySelector('.modal-box').focus();
    }, 100);
}

// Cerrar el modal
function closePoemaModal() {
    const modal = document.getElementById('poema-modal');
    modal.classList.remove('active');
    document.body.style.overflow = 'auto';
}

// Configurar eventos
function setupEventListeners() {
    // Abrir modal al hacer clic en un poema
    document.addEventListener('click', (e) => {
        if (e.target.classList.contains('leer-mas')) {
            e.preventDefault();
            const id = parseInt(e.target.getAttribute('data-id'));
            showPoemaModal(id);
        }
    });

    // Cerrar modal
    document.querySelector('.modal-close').addEventListener('click', (e) => {
        e.preventDefault();
        closePoemaModal();
    });

    // Cerrar modal al hacer clic fuera
    document.getElementById('poema-modal').addEventListener('click', (e) => {
        if (e.target === document.getElementById('poema-modal')) {
            closePoemaModal();
        }
    });

    // Cerrar con ESC
    document.addEventListener('keydown', (e) => {
        if (e.key === 'Escape' && document.getElementById('poema-modal').classList.contains('active')) {
            closePoemaModal();
        }
    });
}

// Inicializar la p√°gina
function init() {
    createStars();
    createPetals();
    renderPoemas();
    setupEventListeners();

    // Efecto de carga suave
    setTimeout(() => {
        document.body.style.opacity = '1';
    }, 100);
}

// Iniciar cuando el DOM est√© listo
document.addEventListener('DOMContentLoaded', init);
